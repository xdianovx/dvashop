    <div>
        <h3 class="font-semibold text-gray-950 dark:text-white">Какой файл можно загрузить</h3>
        <p>Поддерживаются файлы <strong>CSV</strong> и <strong>XLSX</strong>. Файл должен быть таблицей каталога: одна строка — одно поколение автомобиля, а колонки деталей показывают, какие товары доступны для этого автомобиля.</p>
    </div>

    <div>
        <h3 class="font-semibold text-gray-950 dark:text-white">Структура таблицы</h3>
        <ul class="list-disc space-y-1 ps-5">
            <li><strong>Первая строка</strong> — верхние группы деталей, например «Арка» или «Пенка».</li>
            <li><strong>Вторая строка</strong> — названия деталей внутри группы.</li>
            <li><strong>Колонка A</strong> — фото автомобиля. Если там ссылка, импорт поставит фото поколения авто в очередь обработки.</li>
            <li><strong>Колонки B-F</strong> — марка, модель, поколение, годы и кузов.</li>
            <li><strong>Колонки G+</strong> — детали/товары.</li>
        </ul>
    </div>

    <div>
        <h3 class="font-semibold text-gray-950 dark:text-white">Что означают значения в ячейках деталей</h3>
        <ul class="list-disc space-y-1 ps-5">
            <li><strong>1, 1.0, да, yes, true</strong> — товар есть. Если URL товара не указан, импорт привяжет дефолтную картинку детали из <code>public/img/products_default</code>, если она найдена.</li>
            <li><strong>Ссылка</strong> — товар есть, а указанная ссылка будет обработана как фото товара. Дефолтная картинка вместо URL не подставляется.</li>
            <li><strong>Пусто, 0, нет, no, false, -</strong> — товара нет, ячейка пропускается.</li>
        </ul>
    </div>

    <div>
        <h3 class="font-semibold text-gray-950 dark:text-white">Названия деталей</h3>
        <p>Если у колонки есть верхняя группа и нижнее название, товар получает полное название детали. Например: <strong>Арка + Внутренняя универсальная</strong> станет товаром <strong>Арка внутренняя универсальная для Acura TSX...</strong>. При этом категория остаётся древовидной: <strong>Арка / Внутренняя универсальная</strong>.</p>
    </div>

    <div>
        <h3 class="font-semibold text-gray-950 dark:text-white">Дефолтные изображения и галерея</h3>
        <p>Дефолтные картинки берутся из <code>public/img/products_default</code>. Они не копируются в storage на каждый товар: товар хранит ссылку на общий файл. После импорта в карточке товара можно управлять галереей: добавлять ручные изображения, выбирать главное, менять порядок, скрывать/показывать изображения и сбрасывать галерею к дефолтной.</p>
    </div>

    <div>
        <h3 class="font-semibold text-gray-950 dark:text-white">Повторный импорт и ручные данные</h3>
        <p>Повторный импорт обновляет товары, созданные этим же импортом, и добавляет новые строки. Он не должен удалять ручные товары, ручные изображения, ручное главное изображение, ручную цену, SKU, описание, SEO, характеристики и остатки. Товары, которых больше нет в файле, могут быть архивированы только если они были созданы этим же источником импорта и импорт завершился без ошибок.</p>
    </div>
